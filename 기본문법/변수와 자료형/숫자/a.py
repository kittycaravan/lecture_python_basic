"""
파이썬에서 숫자형 데이터 타입으로는 정수형(int), 실수형(float), 복소수형(complex)이 있습니다.	
	
정수형은 소수점이 없는 정수를 나타내며, 다음과 같이 생성할 수 있습니다.	
	x = 10
	y = -5
	
실수형은 소수점을 가지는 숫자를 나타내며, 다음과 같이 생성할 수 있습니다.	
	x = 3.14
	y = -2.5
	
복소수형은 실수부와 허수부를 가지는 숫자를 나타내며, 다음과 같이 생성할 수 있습니다.	
	x = 3 + 4j
	y = -2j
	
파이썬에서는 기본적으로 연산자를 이용한 산술 연산이 지원됩니다. 	
"""
x = 10
y = 3

print(x + y)  # 13
print(x - y)  # 7
print(x * y)  # 30
print(x / y)  # 3.3333333333333335
print(x // y)  # 3
print(x % y)  # 1
print(x ** y)  # 1000